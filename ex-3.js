// ให้สร้าง Function multiplicationTable Function นี้จะรับตัวเลขเข้ามา 1 ตัว แล้วแสดงตารางสูตรคูณตั้งแต่ 1 จนถึงตัวเลขที่ใส่เข้ามา 
// หรือก็คือแสดงสูตรคูณขนาน n x n เช่น ถ้า n เป็น 5
// - แถวแรกแสดงสูตรคูณของ 1 โดยไล่ไปจนครบจำนวน n จะได้ 1 x 1, 1 x 2, 1 x 3, 1 x 4, 1 x 5
// - แถวที่สองแสดงสูตรคูณของ 2 โดยไล่ไปจนครบจำนวน n จะได้ 2 x 1, 2 x 2, 2 x 3, 2 x 4, 2 x 5
// - และทำต่อไปจนถึงแถวที่ 5 ซึ่งแสดงสูตรคูณของ 5 เมื่อไล่ไปจนครบจะได้ 5 x 1, 5 x 2, 5 x 3, 5 x 4, 5 x 5
// - Function นี้ต้องสามารถทำงานได้กับค่า n ที่แตกต่างกัน เช่น
//     - ถ้าใส่ 3 ก็จะได้ตารางขนาด 3 x 3
//     - ถ้าใส่ 10 ก็จะได้ตารางขนาด 10 x 10
// เริ่มเขียนโค้ดตรงนี้ 
function multiplicationTable(n) {
    let finalResult = "";
    for (let i = 1; i <= n; i++) {
        let rowResult = ""; // รวบค่าจากการวน Loop ภายใน
        for (let j = 1; j <= n; j++) {
            rowResult += `${i * j}\t`; // เพิ่มค่าผลลัพธ์ของการคูณ
        }
        finalResult += rowResult  + "\n"; // เพิ่มบรรทัดใหม่หลังจากแต่ละแถว
    }
    return finalResult;
}

console.log(multiplicationTable(5));